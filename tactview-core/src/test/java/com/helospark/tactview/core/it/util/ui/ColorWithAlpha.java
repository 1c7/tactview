package com.helospark.tactview.core.it.util.ui;

public class ColorWithAlpha {
    int red, green, blue, alpha;

    public ColorWithAlpha(int red, int green, int blue, int alpha) {
        this.red = red;
        this.green = green;
        this.blue = blue;
        this.alpha = alpha;
    }

    public int getRed() {
        return red;
    }

    public int getGreen() {
        return green;
    }

    public int getBlue() {
        return blue;
    }

    public int getAlpha() {
        return alpha;
    }

}
